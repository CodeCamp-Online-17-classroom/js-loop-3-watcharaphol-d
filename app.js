for (let i = 0; i < 5; i++) alert(i);
// Result: 0, 1, 2, 3, 4

for (let j = 0; j < 5; ++j) alert(j);
// Result: 0, 1, 2, 3, 4

// Question: Code ทั้งสองจะแสดง alert ออกมาเหมือนกันทั้งหมดหรือไม่
// Answer: แสดงผลลัพธ์ออกมาเหมือนกัน
// เนื่องจากตัว for loop หลังจากที่ทำคำสั่งที่อยู่ใน for แล้ว
// จะทำคำสั่งเพิ่มค่า i++ หรือ ++i ให้จบก่อนโดยการเพิ่มค่า i ไป 1
// เมื่อเพิ่มค่า i จบแล้วก็ค่อยเช็คเงื่อนไขว่ายังสอดคล้องกับเงื่อนไข for อยู่หรือไม่
// ผลลัพธ์ของ for loop จึงเหมือนกัน